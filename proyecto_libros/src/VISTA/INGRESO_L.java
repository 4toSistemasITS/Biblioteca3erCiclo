    private void ActualizarBTNVolverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ActualizarBTNVolverActionPerformed
        // TODO add your handling code here:
        if (TITULO.getText().isEmpty()||AUTOR.getText().isEmpty()||CODIGO.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "No se puede continuar.\nExisten campos vacios.");
        }else{
            try {
                lista2=c.leerFSLibros(archivo);
                for (ModeloLibros e2: lista2){
                    if (e2.getCodigo().equals(CODIGO.getText())){
                        e2.setTitulo(TITULO.getText());
                        e2.setAutor(AUTOR.getText());
                    }
                }
                //Aqui escribo m nuevo array list modificado a mi archivo
                c.ActualizarFSLibros(lista2, archivo);
                lista2.clear();
                INTERFAZ in=new INTERFAZ();
                in.NomBibli1=NomBibli5;
                in.ApeBibli1=ApeBibli5;
                in.setVisible(true);
                dispose();
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(INGRESO_L.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(INGRESO_L.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_ActualizarBTNVolverActionPerformed

