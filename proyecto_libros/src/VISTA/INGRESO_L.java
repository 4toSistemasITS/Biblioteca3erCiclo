    private void TITULOKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TITULOKeyTyped
        // TODO add your handling code here:
         //Accion para convertir todo lo que se escribe en mayuscula
        char c=evt.getKeyChar();
        if(Character.isLowerCase(c)){
            String Buscador=(""+c).toUpperCase();
            c=Buscador.charAt(0);
            evt.setKeyChar(c);
        }
    }//GEN-LAST:event_TITULOKeyTyped

    private void AUTORKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_AUTORKeyTyped
        // TODO add your handling code here:
         //Accion para convertir todo lo que se escribe en mayuscula
        char c=evt.getKeyChar();
        if(Character.isLowerCase(c)){
            String Buscador=(""+c).toUpperCase();
            c=Buscador.charAt(0);
            evt.setKeyChar(c);
        }
        //codigo para que solo acepte letras
        if ((evt.getKeyChar()<'A'||evt.getKeyChar()>'Z')&&(evt.getKeyChar()<'&'||evt.getKeyChar()>'&')&&(evt.getKeyChar()<' '||evt.getKeyChar()>' ')){
            evt.consume();
	}
    }//GEN-LAST:event_AUTORKeyTyped

