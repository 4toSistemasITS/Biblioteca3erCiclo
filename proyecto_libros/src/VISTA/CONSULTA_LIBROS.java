    private void TITULOKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TITULOKeyReleased

        try {
            lista2=c.leerFSLibros(archivo2);
            this.LimpiarTabla();
            for (ModeloLibros e: lista2){
                if(e.BuscarLibro(TITULO.getText())){
                    String[] datos = new String[4];
                    datos[cont] = e.getTitulo();
                    cont += 1;
                    datos[cont] = e.getAutor();
                    cont += 1;
                    datos[cont] = e.getCodigo();
                    cont =0;
                    modelotabla.addRow(datos);   
                }
            }
            lista2.clear();
            //codigo para deshabilitar el boton en caso de que existan campos escritos en el textfile TITULO
            if(TITULO.getText().length()==0){
                LISTADO.setEnabled(true);
            }else{
                LISTADO.setEnabled(false);
            }
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(CONSULTA_LIBROS.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(CONSULTA_LIBROS.class.getName()).log(Level.SEVERE, null, ex);
        }
            



    }//GEN-LAST:event_TITULOKeyReleased

    private void TITULOKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TITULOKeyTyped
        // TODO add your handling code here:
        //Accion para convertir todo lo que se escribe en mayuscula
        char c=evt.getKeyChar();
        if(Character.isLowerCase(c)){
            String Buscador=(""+c).toUpperCase();
            c=Buscador.charAt(0);
            evt.setKeyChar(c);
        }
    }//GEN-LAST:event_TITULOKeyTyped

