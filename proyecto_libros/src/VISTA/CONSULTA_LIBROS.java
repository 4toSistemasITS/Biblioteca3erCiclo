    private void INFORMACIÓNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_INFORMACIÓNActionPerformed
        // TODO add your handling code here:
        fila_seleccionada=TablaLibros.getSelectedRow();
        if (fila_seleccionada!=-1){
            TituloJP=(String) TablaLibros.getValueAt(fila_seleccionada, 0);
            AutorJP=(String) TablaLibros.getValueAt(fila_seleccionada, 1);
            CodigoJP=(String) TablaLibros.getValueAt(fila_seleccionada, 2);
            JOptionPane.showMessageDialog(null,"Los Datos son los siguientes: \n"+"Titulo del Libro: "+TituloJP+"\nAutor del Libro: "+AutorJP+"\nCodigo del Libro: "+CodigoJP,"INFORMACIÓN",JOptionPane.INFORMATION_MESSAGE,new ImageIcon("src/MULTIMEDIA/icon.png"));
        }else{
            JOptionPane.showMessageDialog(null, "No se selecciono ninguna fila", "AVISO", JOptionPane.INFORMATION_MESSAGE, new ImageIcon("src/MULTIMEDIA/alerta.png"));
        }
    }//GEN-LAST:event_INFORMACIÓNActionPerformed

